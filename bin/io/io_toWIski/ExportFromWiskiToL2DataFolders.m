%ExportFromWiskiToL2Data
[fullStationList,saveStationList] = getStations
%
parfor i = 1:length(fullStationList)

    stationName = char(string(fullStationList(i)));
    saveStationName = char(string(saveStationList(i)));

    for y = 1994:2023
        getDataFrom = datetime(y,01,01,00,00,00,00);
        getDataTo = datetime(y,12,31,00,00,00,00);
        savedata = 'yes'
        
        GAWS_L2_WData = readDataFromWiskiToTimetable_dev(stationName,...
            getDataFrom,...
            getDataTo,...
            savedata,...
            saveStationName)
    end
end
